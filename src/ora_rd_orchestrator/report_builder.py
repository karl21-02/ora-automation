def run_qa_verification_via_llm(
    top_topics: list[dict],
    states: dict[str, TopicState],
    scores: dict[str, dict[str, float]],
    llm_command: str,
    llm_timeout: float = 30.0,
) -> list[dict]:
    """Run QA verification on selected topics via LLM."""
    topics_payload = _build_topic_payload(
        top_topics, states, scores=scores,
        max_evidence_snippets=4, include_code_doc=True, include_agent_scores=True,
    )

    payload = {
        "version": "llm-qa-v1",
        "output_contract": {
            "verification": [{
                "topic_id": "string",
                "passed": "bool",
                "evidence_quality": "0-10",
                "concerns": ["string"],
                "recommendation": "string",
                "confidence": "0-1",
            }],
        },
        "topics": topics_payload,
    }

    result = run_llm_command(
        payload=payload,
        command=llm_command,
        timeout=llm_timeout,
    )

    if result.status != "ok":
        logger.warning("LLM QA verification failed: %s", result.parsed.get("reason", "unknown"))
        return []

    verification = result.parsed.get("verification", [])
    if not isinstance(verification, list):
        return []
    return [item for item in verification if isinstance(item, dict)]


