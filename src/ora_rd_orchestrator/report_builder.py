def build_global_scores(
    scores: dict[str, dict[str, float]],
    agent_weights: dict[str, float] | None = None,
) -> list[tuple[str, float]]:
    weights = agent_weights or AGENT_FINAL_WEIGHTS
    agents = set(weights.keys())
    topic_votes = _rank_from_scores(scores, agents)
    totals: list[tuple[str, float]] = []
    for topic_id in scores:
        weighted_sum = 0.0
        for agent_id, agent_weight in weights.items():
            weighted_sum += agent_weight * scores[topic_id].get(_agent_score_key(agent_id), 0.0)
        weighted_sum += 0.02 * topic_votes[topic_id]
        total = round(weighted_sum, 2)
        totals.append((topic_id, total))
    return sorted(totals, key=lambda item: item[1], reverse=True)


def _rank_from_scores(
    scores: dict[str, dict[str, float]],
    agents: set[str] | None = None,
) -> dict[str, int]:
    agents = agents or set(AGENT_FINAL_WEIGHTS.keys())
    by_agent = {
        agent: sorted(
            scores.items(),
            key=lambda i: i[1].get(_agent_score_key(agent), 0.0),
            reverse=True,
        )
        for agent in agents
    }
    rank_map = {topic_id: 0 for topic_id in scores}
    for agent_scores in by_agent.values():
        for rank, (topic_id, _) in enumerate(agent_scores, start=1):
            rank_map[topic_id] += max(0, 20 - rank)
    return rank_map


def build_final_score(
    states: dict[str, TopicState],
    scores: dict[str, dict[str, float]],
    agent_weights: dict[str, float] | None = None,
    agent_ids: list[str] | None = None,
) -> list[dict]:
    weights = agent_weights or AGENT_FINAL_WEIGHTS
    agents = set(weights.keys())
    topic_votes = _rank_from_scores(scores, agents)
    all_agent_ids = agent_ids or sorted(weights.keys())
    output: list[dict] = []

    for topic_id, state in states.items():
        feature = state.compute_features()
        total = 0.0
        for agent_id, agent_weight in weights.items():
            total += agent_weight * scores[topic_id].get(_agent_score_key(agent_id), 0.0)
        total = round(total + 0.02 * topic_votes[topic_id], 2)
        row: dict[str, object] = {
            "topic_id": topic_id,
            "topic_name": state.topic_name,
            "total_score": total,
            "features": feature,
            "project_count": state.project_count,
            "evidence_count": len(state.evidence),
            "evidence": [e.snippet for e in state.evidence[:4]],
        }
        for agent_id in all_agent_ids:
            row[_agent_score_key(agent_id)] = scores[topic_id].get(_agent_score_key(agent_id), 0.0)
        output.append(row)
    output.sort(key=lambda x: x["total_score"], reverse=True)
    return output


def build_agent_rankings(
    scores: dict[str, dict[str, float]],
    top_k: int,
    agent_filter: set[str] | None = None,
) -> dict[str, list[str]]:
    agents = agent_filter if agent_filter else set(AGENT_FINAL_WEIGHTS.keys())
    rankings: dict[str, list[str]] = {}
    for agent in agents:
        key = _agent_score_key(agent)
        ranked_agent = sorted(scores.items(), key=lambda item: item[1].get(key, 0.0), reverse=True)
        rankings[agent] = [topic_id for topic_id, _ in ranked_agent[:top_k]]
    return rankings


def build_consensus_via_llm(
    agent_rankings: dict[str, list[str]],
    ranked: list[dict],
    llm_command: str | None = None,
    llm_timeout: float = 15.0,
    top_k: int = 6,
) -> list[str]:
    """Build consensus ranking via LLM. Falls back to simple aggregation on failure."""
    payload = {
        "version": "llm-consensus-rank-v1",
        "system_prompt": (
            "You are a research committee secretary. "
            "Given each agent's ranked topic preferences, produce a single consensus ranking "
            "that best represents the committee's priorities. "
            "Consider topic scores, diversity of support across agents, and strategic balance. "
            "Return JSON: {\"consensus\": [\"topic_id_1\", \"topic_id_2\", ...]}"
        ),
        "agent_rankings": agent_rankings,
        "topics": [
            {"topic_id": item["topic_id"], "topic_name": item["topic_name"],
             "total_score": item.get("total_score", 0), "features": item.get("features", {})}
            for item in ranked[:top_k * 2]
        ],
        "target_size": top_k,
    }

    if llm_command:
        result = run_llm_command(payload=payload, command=llm_command, timeout=llm_timeout)
        if result.status == "ok":
            ids = result.parsed.get("consensus", [])
            if isinstance(ids, list) and len(ids) >= 1:
                return [str(tid) for tid in ids if isinstance(tid, str)][:top_k]

    # Simple aggregation fallback (no hardcoded weights — just count appearances)
    topic_score: dict[str, float] = {}
    for agent, topics in agent_rankings.items():
        for rank, topic_id in enumerate(topics[:top_k], start=1):
            topic_score[topic_id] = topic_score.get(topic_id, 0.0) + max(0, top_k + 1 - rank)
    return [tid for tid, _ in sorted(topic_score.items(), key=lambda x: x[1], reverse=True)][:top_k]


def build_phase_plan_via_llm(
    ranked: list[dict],
    top_k: int,
    llm_command: str | None = None,
    llm_timeout: float = 15.0,
) -> list[dict]:
    """Build phase plan via LLM. Falls back to score-based distribution on failure."""
    payload = {
        "version": "llm-phase-plan-v1",
        "system_prompt": (
            "You are a strategic R&D planner. Given ranked research topics with their features "
            "(impact, feasibility, novelty, risk_penalty, research_signal), assign each topic "
            "to one of three execution phases:\n"
            "- Phase 1 (Month 1-2): Quick wins with high feasibility and immediate impact\n"
            "- Phase 2 (Month 3-4): Pilot projects needing integration or moderate effort\n"
            "- Phase 3 (Month 5-8): Long-term research tracks with high novelty but more risk\n\n"
            "For each topic, also provide a one-line goal. "
            "Return JSON: {\"phases\": [{\"phase\": \"Month 1-2\", \"topics\": "
            "[{\"rank\": 1, \"topic_id\": \"...\", \"topic\": \"...\", \"score\": 7.5, \"goal\": \"...\"}]}, ...]}"
        ),
        "topics": [
            {
                "rank": idx,
                "topic_id": item["topic_id"],
                "topic_name": item["topic_name"],
                "total_score": item.get("total_score", 0),
                "features": item.get("features", {}),
            }
            for idx, item in enumerate(ranked[:top_k], start=1)
        ],
    }

    if llm_command:
        result = run_llm_command(payload=payload, command=llm_command, timeout=llm_timeout)
        if result.status == "ok":
            phases = result.parsed.get("phases", [])
            if isinstance(phases, list) and len(phases) >= 1:
                return [p for p in phases if isinstance(p, dict) and p.get("topics")]

    # Fallback: distribute evenly by rank (no hardcoded thresholds)
    phase_labels = ["Month 1-2", "Month 3-4", "Month 5-8"]
    items = ranked[:top_k]
    n = len(items)
    chunk = max(1, (n + 2) // 3)
    phases: list[dict] = []
    for i, label in enumerate(phase_labels):
        group = items[i * chunk:(i + 1) * chunk]
        if not group:
            continue
        phases.append({
            "phase": label,
            "topics": [
                {
                    "rank": i * chunk + j + 1,
                    "topic_id": item["topic_id"],
                    "topic": item["topic_name"],
                    "score": item.get("total_score", 0),
                    "goal": "Prioritized by overall score ranking",
                }
                for j, item in enumerate(group)
            ],
        })
    return phases


