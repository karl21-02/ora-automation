def to_json(
    states: dict[str, TopicState],
    scores: dict[str, dict[str, float]],
    scores_initial: dict[str, dict[str, float]] | None,
    ranked: list[dict],
    phases: list[dict],
    report_focus: str,
    version_tag: str,
    research_sources: list[dict[str, str]],
    topic_keywords: dict[str, list[str]] | None = None,
    selected_agent_decisions: dict[str, list[dict]] | None = None,
    discussion: list[dict] | None = None,
    debate_rounds: int = DEBATE_ROUNDS_DEFAULT,
    consensus_summary: dict | None = None,
    orchestration_profile: str = ORCHESTRATION_PROFILE_DEFAULT,
    orchestration_stages: list[str] | None = None,
    service_scope: list[str] | None = None,
    feature_scope: list[str] | None = None,
    pipeline_decisions: list[dict] | list[OrchestrationDecision] | None = None,
    pipeline_stage_log: list[dict] | None = None,
    hierarchical_analysis: dict | None = None,
    strategy_cards_prebuilt: list[dict] | None = None,
    qa_verification: list[dict] | None = None,
    # --- new report sections (LLM-generated) ---
    executive_summary: dict | None = None,
    asis_analysis: dict | None = None,
    tobe_direction: dict | None = None,
    feasibility_evidence: dict | None = None,
    # --- precomputed values (avoid recomputation) ---
    precomputed_agent_rankings: dict[str, list[str]] | None = None,
    precomputed_research_queries: list[dict[str, object]] | None = None,
) -> dict:
    if precomputed_agent_rankings is not None:
        agent_rankings_map = precomputed_agent_rankings
    else:
        agent_rankings_map = build_agent_rankings(scores, top_k=max(1, len(ranked)))
    consensus = []
    if consensus_summary:
        consensus = list(consensus_summary.get("final_consensus_ids", []))
    if precomputed_research_queries is not None:
        research_queries = precomputed_research_queries
    else:
        from .research import build_research_queries
        research_queries = build_research_queries(ranked, topic_keywords=topic_keywords, top_k=min(6, len(ranked)))
    selected = ranked[:len(ranked)]
    agent_decisions = selected_agent_decisions or {}
    orchestration_stages = _normalize_stages(orchestration_stages, fallback=ORCHESTRATION_STAGES_DEFAULT)
    pipeline_decisions = pipeline_decisions or []
    stage_log = pipeline_stage_log or []
    decision_records = [
        decision.to_dict() if isinstance(decision, OrchestrationDecision) else dict(decision)
        for decision in pipeline_decisions
    ]
    strategy_cards = strategy_cards_prebuilt or _build_minimal_cards(selected, states, agent_decisions, research_sources)
    result = {
        "report_version": version_tag,
        "report_focus": report_focus,
        "generated_at": dt.datetime.now().isoformat(),
        "topics": [states[k].to_dict() for k in sorted(states)],
        "research_sources": research_sources,
        "agent_scores": scores,
        "agent_scores_initial": scores_initial or scores,
        "debate_rounds_requested": debate_rounds,
        "debate_rounds_executed": len(discussion or []),
        "discussion": discussion or [],
        "strategy_cards": strategy_cards,
        "qa_verification": qa_verification or [],
        "agent_rankings": agent_rankings_map,
        "agent_decisions": agent_decisions,
        "consensus": consensus,
        "consensus_summary": consensus_summary,
        "research_queries": research_queries,
        "ranked": ranked,
        "phases": phases,
        "orchestration": {
            "profile": orchestration_profile,
            "stages": orchestration_stages,
            "service_scope": service_scope or [],
            "feature_scope": feature_scope or [],
            "pipeline_decisions": decision_records,
            "stage_log": stage_log,
        },
    }
    # New LLM-generated report sections (preserved in JSON)
    if executive_summary:
        result["executive_summary"] = executive_summary
    if asis_analysis:
        result["asis_analysis"] = asis_analysis
    if tobe_direction:
        result["tobe_direction"] = tobe_direction
    if feasibility_evidence:
        result["feasibility_evidence"] = feasibility_evidence
    if hierarchical_analysis:
        result["hierarchical_analysis"] = hierarchical_analysis

    # Section completion metadata — tells consumers which LLM sections succeeded
    result["section_status"] = {
        "executive_summary": "ok" if executive_summary else "skipped",
        "asis_analysis": "ok" if asis_analysis else "skipped",
        "tobe_direction": "ok" if tobe_direction else "skipped",
        "feasibility_evidence": "ok" if feasibility_evidence else "skipped",
        "strategy_cards": "ok" if strategy_cards_prebuilt else "fallback",
        "qa_verification": "ok" if qa_verification else "skipped",
    }
    return result
