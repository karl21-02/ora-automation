def _build_aggregate_stats(
    states: dict[str, TopicState],
    scores: dict[str, dict[str, float]] | None = None,
    research_sources: list[dict[str, str]] | None = None,
) -> dict[str, Any]:
    """Build aggregate statistics for grounding LLM prompts."""
    total_evidence = sum(len(s.evidence) for s in states.values())
    total_code = sum(s.code_hits for s in states.values())
    total_doc = sum(s.doc_hits for s in states.values())
    total_projects = len({p for s in states.values() for p in s.project_hits})
    source_count = len(research_sources or [])
    verified_sources = sum(
        1 for s in (research_sources or []) if s.get("status") == "search_verified"
    )

    score_values: list[float] = []
    if scores:
        for per_topic in scores.values():
            for v in per_topic.values():
                try:
                    score_values.append(float(v))
                except (TypeError, ValueError):
                    pass

    stats: dict[str, Any] = {
        "total_topics": len(states),
        "total_evidence_snippets": total_evidence,
        "total_code_hits": total_code,
        "total_doc_hits": total_doc,
        "unique_projects": total_projects,
        "research_sources_total": source_count,
        "research_sources_verified": verified_sources,
    }
    if score_values:
        stats["score_distribution"] = {
            "min": round(min(score_values), 2),
            "max": round(max(score_values), 2),
            "mean": round(sum(score_values) / len(score_values), 2),
            "count": len(score_values),
        }
    return stats


def generate_strategy_cards_via_llm(
    top_topics: list[dict],
    states: dict[str, TopicState],
    llm_command: str,
    llm_timeout: float = 30.0,
    agent_decisions: dict[str, list[dict]] | None = None,
    sources: list[dict[str, str]] | None = None,
) -> list[dict]:
    """Generate strategy cards entirely via LLM."""
    topics_payload = _build_topic_payload(
        top_topics, states, agent_decisions=agent_decisions,
        include_projects=True, include_agent_decisions=True,
    )

    payload = {
        "version": "llm-strategy-v1",
        "aggregate_stats": _build_aggregate_stats(states, research_sources=sources),
        "output_contract": {
            "strategy_cards": [{
                "topic_id": "string",
                "innovation_ideas": [{"idea": "string", "differentiator": "string", "expected_impact": "string"}],
                "competitive_edge": "string",
                "cause_analysis": ["string"],
                "action_plan": [{"phase": "string", "tasks": ["string"]}],
                "success_metrics": ["string"],
                "expected_impact": "string",
                "risk_mitigation": "string",
            }],
        },
        "topics": topics_payload,
        "research_sources": (sources or [])[:20],
    }

    result = run_llm_command(
        payload=payload,
        command=llm_command,
        timeout=llm_timeout,
    )

    if result.status != "ok":
        logger.warning("LLM strategy card generation failed: %s", result.parsed.get("reason", "unknown"))
        return _build_minimal_cards(top_topics, states, agent_decisions, sources)

    cards_raw = result.parsed.get("strategy_cards", [])
    if not isinstance(cards_raw, list) or not cards_raw:
        logger.warning("LLM returned empty strategy cards")
        return _build_minimal_cards(top_topics, states, agent_decisions, sources)

    cards = []
    for idx, card in enumerate(cards_raw, start=1):
        if not isinstance(card, dict):
            continue
        topic_id = card.get("topic_id", "")
        state = states.get(topic_id)
        if not state:
            continue
        # Find matching item from top_topics
        matched_item = next((item for item in top_topics if item["topic_id"] == topic_id), None)
        cards.append({
            "rank": idx,
            "topic_id": topic_id,
            "topic_name": matched_item["topic_name"] if matched_item else state.topic_name,
            "score": matched_item.get("total_score", 0) if matched_item else 0,
            "feature_profile": matched_item.get("features", {}) if matched_item else {},
            "evidence_count": len(state.evidence),
            "projects": state.project_hits,
            "project_snapshot": _top_projects_summary(state),
            "innovation_ideas": card.get("innovation_ideas", []),
            "competitive_edge": card.get("competitive_edge", ""),
            "cause_analysis": card.get("cause_analysis", []),
            "action_plan": card.get("action_plan", []),
            "success_metrics": card.get("success_metrics", []),
            "expected_impact": card.get("expected_impact", ""),
            "risk_mitigation": card.get("risk_mitigation", ""),
            "agent_decisions": (agent_decisions or {}).get(topic_id, []),
            "evidence_snapshots": [
                {"file": e.file, "line": e.line_no, "snippet": e.snippet}
                for e in state.evidence[:8]
            ],
            "papers": [],
        })
    return cards


def _build_minimal_cards(
    top_topics: list[dict],
    states: dict[str, TopicState],
    agent_decisions: dict[str, list[dict]] | None = None,
    sources: list[dict[str, str]] | None = None,
) -> list[dict]:
    """Minimal card structure when LLM fails (no hardcoded content)."""
    source_map: dict[str, list[dict[str, str]]] = {}
    for item in sources or []:
        key = item.get("topic_id", "")
        source_map.setdefault(key, []).append(item)

    cards: list[dict] = []
    for idx, item in enumerate(top_topics, start=1):
        topic_id = item["topic_id"]
        state = states.get(topic_id)
        if not state:
            continue
        cards.append({
            "rank": idx,
            "topic_id": topic_id,
            "topic_name": item["topic_name"],
            "score": item.get("total_score", 0),
            "feature_profile": item.get("features", {}),
            "evidence_count": len(state.evidence),
            "projects": state.project_hits,
            "project_snapshot": _top_projects_summary(state),
            "innovation_ideas": [],
            "competitive_edge": "",
            "cause_analysis": [],
            "action_plan": [],
            "success_metrics": [],
            "expected_impact": "",
            "risk_mitigation": "",
            "agent_decisions": (agent_decisions or {}).get(topic_id, []),
            "evidence_snapshots": [
                {"file": e.file, "line": e.line_no, "snippet": e.snippet}
                for e in state.evidence[:8]
            ],
            "papers": source_map.get(topic_id, [])[:6],
        })
    return cards


